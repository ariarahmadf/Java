package com.learning;

// Boolean

// Boolean merupakan tipe data primitive yang hanya memiliki dua buah nilai saja, yaitu true dan false.
// Umumnya boolean digunakan untuk melakukan perbandingan dua buah nilai dengan menggunakan sebuah comparison operator

public class Main {
    public static void main(String[] args) {
        System.out.println("Apakah 10 > 9\t: " + (10 > 9)); // Output true, karena 10 lebih besar dari 9
        System.out.println("Apakah 9 == 10\t: " + (9 == 10)); // Output false, karena 9 tidak sama dengan 10
    }
}
