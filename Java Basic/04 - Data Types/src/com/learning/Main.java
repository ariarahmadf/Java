// Data Types 
/* 
    Tipe data umumnya digunakan untuk membedakan setiap nilai yang ada pada sebuah data. 
    Tipe data dibagi kedalam dua kelompok : 
        a. Primitive Data Type (number, char, boolean)
        b. Non-Primitive Data Type (String, Array, Class)
    
    Lalu bedanya apasih dari dua kelompk tipe data tersebut?
        1. Primitive Data Type merupakan type data build in pada java. Sedangkan Non-Primitive Data Type merupakan
        Tipe Data yang dibuat langsung oleh si programmer (kecuali String). 
        
        2. Non-Primitive data type umumnya memiliki fungsi(method) khusus untuk melakukan suatu operasi tertentu, sedangkan
        Primitive Data Type tidak memiliki method.

        3. Primitive Data Type selalu mempunyai default value, sedangkan Non-Primitive Data Type memiliki nilai null.

        4. Primitive Data Type diawali dengan huruf awalannya huruf kecil, sedangkan Non-Primitive Data Type adalah sebaliknya.

        5. Ukuran tipe data Primitive itu berbeda-beda (ukuran boolean berbeda dengan char), sedangkan Non-Primitive Data Type 
        memiliki ukuran yang sama.
*/

public class Main {
    public static void main(String[] args) {
        System.out.println("Hello World");
    }
}
